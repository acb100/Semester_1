public class MathMethods {
    public static void main(String[] args) {
        System.out.println(Math.toRadians(10));
        System.out.println(Math.random());
        System.out.println(Math.round(10.3));
        System.out.println(Math.floor(10.3));
        System.out.println(Math.ceil(10.3));
        System.out.println(Math.sqrt(10.3));
        System.out.println(Math.pow(10, 2));
        System.out.println(Math.PI);
        System.out.println(Math.min(4, 8));
        System.out.println(Math.max(4, 8));
        System.out.println(Math.toRadians(10));
    }
}
